package com.iics.ust.tigercup;

public class ShopData {
    public String name;
    public String category;
    public String img;
    public String lat;
    public String lng;

    public ShopData(String name, String category, String img, String lat, String lng){
        this.name = name;
        this.category = category;
        this.img = img;
        this.lat = lat;
        this.lng = lng;
    }
}
